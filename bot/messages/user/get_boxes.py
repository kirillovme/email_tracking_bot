def show_email_box_information(email_box: dict) -> str:
    """–°–æ–æ–±—â–µ–Ω–∏–µ –æ –≤—ã–≤–æ–¥–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –ø–æ—á—Ç–æ–≤–æ–º —è—â–∏–∫–µ."""
    email_username = email_box.get('email_username', '')
    box_filters = email_box.get('filters', [])
    is_active = email_box.get('is_active', True)
    filters_str = '\n'.join(
        [f"{f['filter_name']} ({f['filter_value']})" for f in box_filters])
    if not filters_str:
        filters_str = '–ù–µ—Ç —Ñ–∏–ª—å—Ç—Ä–æ–≤'
    status = '–ê–∫—Ç–∏–≤–Ω—ã–π' if is_active else '–ü—Ä–∏–æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω'
    return (
        f'–ê–¥—Ä–µ—Å —ç–ª–µ–∫—Ç—Ä–æ–Ω–Ω–æ–π –ø–æ—á—Ç—ã: {email_username}\n'
        f'–°—Ç–∞—Ç—É—Å* –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –Ω–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π: {status}\n'
        '–ü—Ä–∏–º–µ–Ω–µ–Ω—ã —Å–ª–µ–¥—É—é—â–∏–µ —Ñ–∏–ª—å—Ç—Ä—ã:\n'
        f'{filters_str}'
        '\n* –°–º–µ–Ω–∞ —Å—Ç–∞—Ç—É—Å–∞ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –≤ —Ç–µ—á–µ–Ω–∏–µ –º–∏–Ω—É—Ç—ã ‚è∞'
    )


